"""
En Python, podemos almacenar varios valores juntos en una sola variable. Si bien hay muchas formas de hacerlo,
 la más popular es la lista .

Es muy similar a una cadena ya que una cadena es una colección de caracteres. Una lista también es solo una colección
 de valores. Sin embargo, los valores pueden ser de cualquier tipo.

Lo único que tenemos que hacer es encerrar todos los elementos entre corchetes []y separarlos con comas.
"""
my_list = [1, 2.5, "A string", True]
print(my_list)
print(my_list[2])
