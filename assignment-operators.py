"""
Esta es una categoría de operadores que se utiliza para asignar valores a una variable. El =operador es un operador de asignación, pero no el único.
"""


"""

Operador	Objetivo	                                Notación
=	        Asignar	                                    Infijo
+=	        Agregar y asignar	                        Infijo
-=	        Restar y asignar	                        Infijo
*=	        Multiplicar y asignar	                    Infijo
/=	        Dividir y asignar	                        Infijo
//=	        Dividir, piso y asignar	                    Infijo
**=	        Aumentar el poder y asignar	                Infijo
%=	        Tomar módulo y asignar	                    Infijo
|=	        O y Asignar	                                Infijo
&=	        Y y Asignar	                                Infijo
^=	        XOR y asignar	                            Infijo
>>=     	Desplazamiento a la derecha y asignación	Infijo
<<=	        Desplazamiento a la izquierda y asignación	Infijo

"""

num = 10
print(num)

num += 5
print(num)

num -= 5
print(num)

num *= 2
print(num)

num /= 2
print(num)

num **= 2
print(num)

